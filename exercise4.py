number1 = int (input (" add the first number "))
number2 = int (input (" add the second number "))
operator = input (" enter the operator ")

if operator == "+":
    print(number1,'+',number2,'=', number1 + number2)
if operator == "-":
    print(number1, '-', number2, '=', number1, number2)

if operator == "*":
    print(number1, '*', number2, '=', number1, number2)

if operator == "/":
    print(number1, '/', number2, '=', number1, number2)

